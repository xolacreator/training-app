#!/usr/bin/env node
// Build step: serialise the modular running knowledge library into an inline
// RUNNING_KB constant in index.html (the single-file PWA can't import at runtime).
//
//   node scripts/build-knowledge.mjs          # regenerate the inline block
//   node scripts/build-knowledge.mjs --check   # CI: non-zero if index.html is stale
//
// Edit knowledge/running/library.mjs (the source); never hand-edit the generated block.
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { RUNNING_DOMAINS, COACH_EV_PHILOSOPHY, RUNNING_KB_VERSION } from '../knowledge/running/library.mjs';

const __dir = dirname(fileURLToPath(import.meta.url));
const HTML = join(__dir, '..', 'index.html');
const START = '/* RUNNING_KB:START (generated by scripts/build-knowledge.mjs — do not edit) */';
const END   = '/* RUNNING_KB:END */';

const kb = {
  version: RUNNING_KB_VERSION,
  domains: RUNNING_DOMAINS,
  philosophy: COACH_EV_PHILOSOPHY,
};
const block = `${START}\nconst RUNNING_KB=${JSON.stringify(kb)};\n${END}`;

let html = readFileSync(HTML, 'utf8');
const check = process.argv.includes('--check');

const si = html.indexOf(START), ei = html.indexOf(END);
if (si === -1 || ei === -1) {
  console.error('ERROR: RUNNING_KB markers not found in index.html. Add them once:\n' +
    `  ${START}\n  const RUNNING_KB={};\n  ${END}`);
  process.exit(2);
}
const current = html.slice(si, ei + END.length);
if (current === block) {
  console.log(`RUNNING_KB up to date (v${RUNNING_KB_VERSION}, ${RUNNING_DOMAINS.length} domains).`);
  process.exit(0);
}
if (check) {
  console.error(`✗ index.html RUNNING_KB is STALE — run: node scripts/build-knowledge.mjs`);
  process.exit(1);
}
html = html.slice(0, si) + block + html.slice(ei + END.length);
writeFileSync(HTML, html);
console.log(`✓ Inlined RUNNING_KB v${RUNNING_KB_VERSION} — ${RUNNING_DOMAINS.length} domains, ${block.length} bytes.`);
