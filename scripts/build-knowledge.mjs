#!/usr/bin/env node
// Build step: serialise modular knowledge libraries into inline constants in index.html
// (the single-file PWA can't import at runtime). Edit the library sources; never
// hand-edit the generated blocks.
//
//   node scripts/build-knowledge.mjs           # regenerate all inline KB blocks
//   node scripts/build-knowledge.mjs --check   # CI: non-zero if any block is stale
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { RUNNING_DOMAINS, COACH_EV_PHILOSOPHY, RUNNING_KB_VERSION } from '../knowledge/running/library.mjs';
import { STRENGTH_DOMAINS, STRENGTH_COACH_EV, STRENGTH_KB_VERSION } from '../knowledge/strength/library.mjs';
import { ADAPTATION_LIBRARY, ADAPTATION_LIBRARY_VERSION } from '../knowledge/adaptations.mjs';
import { PRESCRIPTION_LIBRARY, PRESCRIPTION_LIBRARY_VERSION } from '../knowledge/prescriptions.mjs';

const __dir = dirname(fileURLToPath(import.meta.url));
const HTML = join(__dir, '..', 'index.html');

const KBS = [
  { name:'RUNNING_KB',  start:'/* RUNNING_KB:START (generated by scripts/build-knowledge.mjs — do not edit) */',  end:'/* RUNNING_KB:END */',
    data:{ version:RUNNING_KB_VERSION, domains:RUNNING_DOMAINS, philosophy:COACH_EV_PHILOSOPHY } },
  { name:'STRENGTH_KB', start:'/* STRENGTH_KB:START (generated by scripts/build-knowledge.mjs — do not edit) */', end:'/* STRENGTH_KB:END */',
    data:{ version:STRENGTH_KB_VERSION, domains:STRENGTH_DOMAINS, philosophy:STRENGTH_COACH_EV } },
  { name:'ADAPTATION_KB',  start:'/* ADAPTATION_KB:START (generated by scripts/build-knowledge.mjs — do not edit) */',  end:'/* ADAPTATION_KB:END */',
    data:{ version:ADAPTATION_LIBRARY_VERSION, adaptations:ADAPTATION_LIBRARY }, key:'adaptations' },
  { name:'PRESCRIPTION_KB', start:'/* PRESCRIPTION_KB:START (generated by scripts/build-knowledge.mjs — do not edit) */', end:'/* PRESCRIPTION_KB:END */',
    data:{ version:PRESCRIPTION_LIBRARY_VERSION, prescriptions:PRESCRIPTION_LIBRARY }, key:'prescriptions' },
];

let html = readFileSync(HTML, 'utf8');
const check = process.argv.includes('--check');
let changed = false, stale = false;

for (const kb of KBS){
  const si = html.indexOf(kb.start), ei = html.indexOf(kb.end);
  if (si === -1 || ei === -1){
    console.error(`ERROR: ${kb.name} markers not found in index.html. Add once:\n  ${kb.start}\n  const ${kb.name}={};\n  ${kb.end}`);
    process.exit(2);
  }
  const listKey = kb.key || 'domains';
  const count = Array.isArray(kb.data[listKey]) ? kb.data[listKey].length : 0;
  const block = `${kb.start}\nconst ${kb.name}=${JSON.stringify(kb.data)};\n${kb.end}`;
  const current = html.slice(si, ei + kb.end.length);
  if (current === block){ console.log(`${kb.name} up to date (v${kb.data.version}, ${count} ${listKey}).`); continue; }
  if (check){ console.error(`✗ ${kb.name} is STALE — run: node scripts/build-knowledge.mjs`); stale = true; continue; }
  html = html.slice(0, si) + block + html.slice(ei + kb.end.length);
  changed = true;
  console.log(`✓ Inlined ${kb.name} v${kb.data.version} — ${count} ${listKey}.`);
}
if (check) process.exit(stale ? 1 : 0);
if (changed) writeFileSync(HTML, html);
